def aumentar(valor, porcentagem):
    novo_valor = valor + ((valor * porcentagem) / 100)
    return novo_valor

def diminuir(valor, porcentagem):
    novo_valor = valor - ((valor * porcentagem) / 100)
    return novo_valor

def dobro(valor):
    return valor * 2

def metade(valor):
    return valor / 2