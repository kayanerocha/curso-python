def aumentar(valor, porcentagem):
    novo_valor = valor + ((valor * porcentagem) / 100)
    return novo_valor

def diminuir(valor, porcentagem):
    novo_valor = valor - ((valor * porcentagem) / 100)
    return novo_valor

def dobro(valor):
    novo_valor = valor * 2
    return novo_valor

def metade(valor):
    novo_valor = valor / 2
    return novo_valor