n = input('Digite algo: ')
print('O tipo primitivo de {}{}{} é {}{}{}'.format('\033[34m', n, '\033[m', '\033[31m', type(n), '\033[m'))
print('É numérico? {}{}{}'.format('\033[32m', n.isnumeric(), '\033[m'))
print('Está em maiúsculo? {}{}{}'.format('\033[35m', n.isupper(), '\033[m'))
print('É um título? {}{}{}'.format('\033[36m', n.istitle(), '\033[m'))
print('Tem somente espaços? {}{}{}'.format('\033[37m', n.isspace(), '\033[m'))
print('Está em minúsculo? {}{}{}'.format('\033[1;31m', n.islower(), '\033[m'))
print('É possível imprimir? {}{}{}'.format('\033[1;32m', n.isprintable(), '\033[m'))
print('É uma palavra reservada? {}{}{}'.format('\033[1;33m', n.isidentifier(), '\033[m'))
print('É um digito? {}{}{}'.format('\033[1;34m', n.isdigit(), '\033[m'))
print('É um número deciamal? {}{}{}'.format('\033[1;35m', n.isdecimal(), '\033[m'))
print('É uma string do tipo ASCII? {}{}{}'.format('\033[1;36m', n.isascii(), '\033[m'))
print('É alfanumérico? {}{}{}'.format('\033[1;37m', n.isalpha(), '\033[m'))
print('É? {}{}{}'.format('\033[1;4;31m', n.isalnum(), '\033[m'))
